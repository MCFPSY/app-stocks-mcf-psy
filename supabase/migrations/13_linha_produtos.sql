-- =========================================================
-- Allow-list de produtos por linha MCF
-- Filtra os dropdowns de produto em "Registos Produção MCF" para mostrar
-- apenas produtos historicamente produzidos em cada linha (≥2 ocorrências
-- na sheet "MCF data" do Report diário produção.xlsm).
--
-- Mapping Excel → app:
--   01.1               → Linha principal T1/T3
--   01.2               → [+] Madeira de 2ª T1/T3
--   02.1               → Linha charriot [B]
--   02.2               → [+] Linha charriot [Tábuas]
--   02.3               → Linha charriot [Duplos]
--   03.1.1 + 03.2.1    → Linha aproveitamentos T1/T3
--   03.1.2 + 03.2.2    → [+] Madeira de 2ª Aprov T1/T3
--   04 + 04.1 + 04.2   → [-] Linha PSY
--   05                 → [-] Linha Aprov - Sobras
--
-- Idempotente. Para acrescentar produtos: INSERT manual ou via UI futura.
-- =========================================================

CREATE TABLE IF NOT EXISTS linha_produtos (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  linha_nome text NOT NULL,
  produto_stock text NOT NULL,
  ativo boolean DEFAULT true,
  criado_em timestamptz DEFAULT now(),
  UNIQUE(linha_nome, produto_stock)
);
CREATE INDEX IF NOT EXISTS idx_linha_produtos_linha ON linha_produtos(linha_nome);

ALTER TABLE linha_produtos ENABLE ROW LEVEL SECURITY;

DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='linha_produtos' AND policyname='read_all_auth') THEN
    CREATE POLICY "read_all_auth" ON linha_produtos FOR SELECT TO authenticated USING (true);
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='linha_produtos' AND policyname='admin_write') THEN
    CREATE POLICY "admin_write" ON linha_produtos FOR ALL TO authenticated
      USING (EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND perfil = 'admin'));
  END IF;
END $$;

